    <section class="section padding-bottom-100">
        <div class="container">
            <div class="row">
                <div class="left-heading">
                    <h2 class="section-title">Características de NodeJS</h2>
                </div>
                <div class="left-text">
                    <p><strong>Asíncrono y controlado por eventos: </strong>todas las API de la biblioteca Node.js son asíncronas, es decir, sin bloqueo. Esencialmente significa que un servidor basado en Node.js nunca espera que una API devuelva datos. El servidor pasa a la siguiente API después de llamarlo y un mecanismo de notificación de Events of Node.js ayuda al servidor a obtener una respuesta de la llamada API anterior.</p>
                </div>
                <div class="left-text">
                    <p><strong>Muy rápido: </strong>al estar construido en el motor JavaScript V8 de Google Chrome, la biblioteca Node.js es muy rápida en la ejecución de código.</p>
                </div>
                <div class="left-text">
                    <p><strong>Sin almacenamiento en búfer: </strong>las aplicaciones Node.js nunca almacenan en búfer ningún dato. Estas aplicaciones simplemente generan los datos en fragmentos.</p>
                </div>
                <div class="left-text">
                    <p><strong>Procesos en un solo hilo pero altamente escalable: </strong>Node.js utiliza un modelo de una solo hilo con bucle de eventos. El mecanismo de eventos ayuda al servidor a responder sin bloqueos y hace que el servidor sea altamente escalable en comparación con los servidores tradicionales que crean hilos limitados para manejar las solicitudes. Node.js utiliza un solo programa de subprocesos y el mismo programa puede proporcionar servicio a un número mucho mayor de solicitudes que los servidores tradicionales como el Servidor Apache HTTP.</p>
                </div>
                <div class="left-text">
                    <p><strong>Licencia basada en código abierto: </strong>Node.js se distribuye gracias a una licencia MIT.</p>
                </div>
            </div>
