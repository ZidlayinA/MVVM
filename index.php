                    <strong>9. Compatible con Frameworks web:  </strong>Spring WebFlux y Spring MVC.<br>
                    <strong>10. Permite el procesamiento de datos por lotes.</strong><br>
                    <strong>11. Administración remota: </strong>a través de este módulo se puede configurar la visibilidad y gestión de los objetos Java para la configuración local o remota vía JMX.<br>
                    <strong>12. Es un framework liviano</strong>debido a su implementación POJO (Plain Old Java Object), Spring Framework no obliga al programador a heredar ninguna clase ni a implementar ninguna interfaz.<br>
