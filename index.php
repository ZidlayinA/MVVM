                    <strong>6. Programación orientada a aspectos (AOP): </strong>permite la implementación de rutinas transversales.<br>
                    <strong>7. Fácil: </strong>Facilita en gran medida la programación basada en MVC (Modelo Vista Controlador) y una implementación rápida basada en Inyección de Dependencias (Dependency Injection).<br>
