                    </div>
                    <div class="left-text">
                        <p>Spring se basa en la filosofía “convención sobre configuración”, reduciendo al mínimo el número de pasos que un desarrollador debe dar en la configuración inicial del proyecto antes de ponerse a trabajar en la parte dura del proyecto centrando sus esfuerzos en lo importante. La idea es clara: no perder el tiempo (y el dinero) en hacer las mismas cosas una y otra vez.</p>
